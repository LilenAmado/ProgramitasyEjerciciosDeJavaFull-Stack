
package Logica;


public class Controladora {
    
    public double sumar(double numTraido1, double numTraido2){
        double resultado = numTraido1 + numTraido2;
        return resultado;
    }
    public double restar(double numTraido1, double numTraido2){
        double resultado = numTraido1 - numTraido2;
        return resultado;
    }
    public double multiplicar(double numTraido1, double numTraido2){
        double resultado = numTraido1 * numTraido2;
        return resultado;
    }
    public double dividir(double numTraido1, double numTraido2){
        double resultado = numTraido1 / numTraido2;
        return resultado;
    }
}
